<p style="color:#2b3e63;">
The Kingdom of Thailand lies in the heart of Southeast Asia, roughly equidistant 
between India and China. Thailand receives visitors with a smiling warmth, 
the hallmark of the East. Bangkok is the 
final successor to capital cities through history that were located in this region,
 on either side of theChao Phraya River, known as the "Rice Bowl of Asia". 
It is the location of virtually all major business, government, culture, 
trade and also where most of the religious monuments are.
 Vast forests of bamboo and hardwood trees, sheltering varieties of
 wildlife, spectacular water-falls and great rivers such as the famous River Kwai, 
characterize the western region.
 Beach resorts, such as Hua Hin and Phuket are located
 in the west and southwest. 
