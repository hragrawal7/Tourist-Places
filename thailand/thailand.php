<p style="color:brown;font-size:1.0em;"> Description of City Tour</p>
<p style="color:#2b3e63;">
Drive pass different commercial and residential areas of Bangkok. Visit the three 
historical temples: 
- Wat Trai Mit, 
well known for its famous Golden Buddha,
 constructed during the Sukhothai period.
 The beautiful image of solid gold is three meters high and weighs five and a
 half tons. 
- Wat Pho, is one of Bangkok's oldest and largest 
temple housing the gigantic gold plated reclining 
Buddha with beautifully inlaid mother-of-pearl
 soles. Wat Pho is also regarded as the first center of public education
 and is sometimes called 'Thailand's first university' .
 Mural paintings, inscriptions, and 
statues fiund in the temple educated people on varied subjects;
 for example, literature, warfare, astronomy, meditation, and Thai traditional massage. 
- Wat Benjamabophit (The Marble Temple) , this magnificent monastery was
 built with white Italian marble from Carara </p>
