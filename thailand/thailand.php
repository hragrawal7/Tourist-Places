Description of Coral Island Tour </p>
<pre style="color:#2b3e63;">
Only two hours from Bangkok,
 the beautiful beaches of Koh Larn - an idyllic island located just west of Pattaya 
- make a perfect day-trip.
 Known as Coral Island and 
covering an area of about 4 square kilometres,
 you spend the day swimming,
 snorkeling
 and sunbathing. Enjoy the abundant coral and underwater world, 
either from the glass-bottom boat or by
 diving in. Price includes
 travel to and from Pattaya, transfer by boat to Koh Larn and Thai lunch.

</pre>
