<p style="font-weight:bold;color:#cc3300;">HYDERABAD</p>
<p style="color:2b3e63;">
The capital city of Andhra Pradesh, Hyderabad, is the fifth largest metropolitan  city in the country. Famous for the Nizams and the cusine, this is a city with a rich historic background. The city is 400 years old.
 In Hyderabad, magnificient forts, majestic palaces, ancient mosques and temples make a blend of the Hindu and Islamic styles of architecture. 
It is also know as Pearl City, because of the pearl and precious stone industry.</p></pre><p style="font-weight:bold;color:#2b3e63;">
HOW TO REACH</p>
<p style="font-weight:bold;color:#cc3300;">
BY AIR</p>
<p style="font-weight:bold;color:#2b3e63;">
Hyderabad airport is well connected to all major cities of India. Only a few International fights link the city to other countries.
</p>
<p style="font-weight:bold;color:#cc3300;">
BY RAIL</p>
<p style="font-weight:bold;color:#2b3e63;">The city has two main railheads, namely,
 Hyderabad and Secunderabad, which are connected to all important cities and towns of India.</p>
<p style="font-weight:bold;color:#2b3e63;">
BY ROAD</p>
<p style="font-weight:bold;color:#2b3e63;">
Hyderabad lies at an intersection of two national highways, NH 7 and NH 9, and is well connected to all major and minor towns of the country.</p>
<p style="font-weight:bold;color:#2b3e63;">
WHAT TO SEE</p>
<p style="font-weight:bold;color:#cc3300;"> 
CHARMINAR</p>
<p style="font-weight:bold;color:#2b3e63;"> 
Charminar is located at the center of the old city and surround by lively bazaars. It was built by Sultan Mohammed Quli Qutb Shah in 1591 in honour of his wife Bhagmati,
 shortly after he had shifted his capital from Golconda to what now is known as Hyderabad. It is popular as the Arc de Triomphe of the East and considered as the
 legendary masterpiece of Qutub Shahi's. Charminar is in square shape with four towers in the four corners of the square, each side is 20 metres long.
 Each arch is 11 metres wide and rises 20 metres to the pinnacle from the plinth. The minarets soar skywards by 24 metres from the roof of Charminar.
 The structure of Charminar reflects both Indian and Mughal architectural styles. One can get good views of the old city from the top of the minarets.</p>
<p style="font-weight:bold;color:#cc3300;"> 
BIRLA MANDIR</p>
<p style="font-weight:bold;color:#2b3e63;"> 
Birla Mandir located in south end of Hussain Sagar in Hyderabad, stands atop the Kala Pahad. The Birlas built this temple in 1976. This modern enchanting temple dedicated to Lord Venkateswara (Vishnu),
 constructed with white marbles from Rajasthan. Birla Mandir combines the architectural styles of Orissan and South Indian temples. The granite image
 of the presiding deity is about 11ft tall and a carved lotus forms an umbrella for this image. Birla Mandir offers a spectacular sight when illuminated at night. </p>
<p style="font-weight:bold;color:#cc3300;"> 
HUSSAIN SAGAR LAKE</p>
<p style="font-weight:bold;color:#2b3e63;"> 
One of the largest man made lakes is situated at Hyderabad. Its uniqueness lies in the fact that it connects the twin cities of Hyderabad and Secunderabad. A famous monolith of Lord Buddha is installed at the center of the lake.
 Hussain Sagar Lake is popularly known as the Tank Bund. The Hussain Sagar was constructed on a tributary of the Musi river by Hussain Shah Wahi during the reign of Ibrahim Quli Qutub Shah in 1562. It is a sprawling a
rtificial lake that holds water perenially. Every Sunday cultural programmes are conducted by the Department of culture in the evenings and admission is free for the public.</p>

<p style="font-weight:bold;color:#cc3300;"> 
SALAR JUNG MUSEUM</p>
<p style="font-weight:bold;color:#2b3e63;"> 
Salar Jung Museum is located on the southern side of the river Musi. It is the third most prominent museum in India domiciliating the largest
 one-man aggregations of antiques in the world. This museum is very famous throughout India for its well-known collections of materials belong 
to various civilizations starting from first century.</p></td></tr>

