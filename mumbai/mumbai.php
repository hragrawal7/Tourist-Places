<p style="color:#cc3300;font-weight:bold;">
 MUMBAI
</p>
<p style="color:#2b3e63;font-weight:bold;">
 Mumbai is the country�s fastest moving, most affluent and industrialized city.
 It  is the largest metropolitan city in India. It is also famous as India�s busiest port, handling about 
50 percent of India�s total forign trade. The city is the home to the famous Bollywood industry and many famous actors
. Also known as the commercial capital of India,
 Mumbai is also famous for the manufacturing units of automobiles, bicycles and pharmaceuticals. 
</p><table width="50%">
