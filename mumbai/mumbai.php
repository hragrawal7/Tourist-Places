<p style="color:#2b3e63;font-weight:bold;">
HOW TO REACH</p>
<p style="color:#cc3300;font-weight:bold;">
BY AIR</p>
<p style="color:#2b3e63;font-weight:bold;">
Chatrapati Shivaji International Airport is the international airport and Santa Cruz is the domestic airport,
 both 5 km apart. Regular shuttle bus services are available between the two airports.
 All domestic and international flights operate regular flights from and to Mumbai.</p>
<p style="color:#cc3300;font-weight:bold;">
BY RAIL</p>
<p style="color:#2b3e63;font-weight:bold;">
The Chattrapati Shivaji Terminus (CST) is the main railway station.
 Dadar, Churchgate and Kurla are the other railway stations in Mumbai. Konkan railways has a very good network, 
which connects Mumbai to the rest of the country.</p>
<p style="color:#cc3300;font-weight:bold;">

BY ROAD</p>
<p style="color:#2b3e63;font-weight:bold;">
State highways and a good network of roads connect Mumbai to other parts of India. Roads to Goa, 
Pune and Nasik are well maintained and have regular bus services by state run as well as private operators.</p>
<p style="color:#2b3e63;font-weight:bold;">
WHAT TO SEE</p>
<p style="color:#cc3300;font-weight:bold;">

CHATRAPATI SHIVAJI TERMINUS</p>
<p style="color:#2b3e63;font-weight:bold;">
The Chhatrapati Shivaji Terminus, formerly known as Victoria Terminus in Mumbai,
 is an outstanding example of Victorian Gothic Revival architecture in India. It is the westernmost end point of the centeral 
railways of India. A magnificent building, completed in 1888, the Victoria Terminus was named after Queen Victoria
. A large section of the building is used for administrative functions of the Central Railways, 
including commercial operations such as railway reservations.
</p>
<p style="color:#cc3300;font-weight:bold;">
PRINCE OF WALES MUSUEM</p>
<p style="color:#2b3e63;font-weight:bold;">
The Prince of Wales museum was built to commemorate King George V's visit to Mumbai,
 it was designed by George Wittet and completed in 1923. It is one of India's finest museums and houses treasures,
 artefacts, paintings and sculpture from the many periods covering India's history, including the Indus Valley Civilization.</p>
<p style="color:#cc3300;font-weight:bold;">
GATEWAY OF INDIA </p>
<p style="color:#2b3e63;font-weight:bold;">
The Gateway of India is a monument located on the waterfront in Apollo Bunder area in South Mumbai. 
It is a basalt arch 26 metres (85 ft) high. In earlier times, the Gateway was the monument that visitors 
arriving by boat would have first seen in the city of Bombay. It is combination and mixture of Hindu and Muslim architecture.</p>
<p style="color:#cc3300;font-weight:bold;">
MARINE DRIVE AND CHOWPATTY BEACH</p>
<p style="color:#2b3e63;font-weight:bold;">
Marine drive is also known as Netaji Subhashchandra Bose Road, with Nariman Point on one end to Babulnath, at the foot of Walkeshwar on the other.
 Chowpatty is situated somewhat in the middle. Chowpatty Beach is a busy and crowded beach, You can find, 
vendors, masseurs and roadside restaurants famous for its bhelpuri and kulfi. Across the Chowpatty Beach area is the Taraporewala Aquarium. 
Marine Drive is also referred to as the Queen's Necklace because of the beautiful line of street lamps which light up at night.</p>
<p style="color:#cc3300;font-weight:bold;">
HAJI ALI SHRINE </p>
<p style="color:#2b3e63;font-weight:bold;">
The mosque can be reahed only in low tide as the path connecting to the mosque gets entirely submerged in water during monsoons. 
In monsoon one gets the impression that entire mosque is floating in the sea and hence Haji Ali is also called the floating mosque
 of Mumbai. The mosque is named after a wealthy muslim merchant Haji Ali, who renounced all his worldly belongings before embarking 
a holy plgrimage to Mecca. The faithful tie little red ribbons on wooden lattices to honour the soul of Haji Ali and to seek his blessings.</p>
<p style="color:#cc3300;font-weight:bold;">
MAHALAXMI TEMPLE</p>
<p style="color:#2b3e63;font-weight:bold;">
Located at the seashore on the northern side of Malabar hills, Mahalakshmi temple is one of 
the most sacred temples in Mumbai. The temple enshrines three dieties namely Lakshmi, Kali and Saraswati.
The Mahalaxmi Temple is a popular holy site as Mahalaxmi is the goddess of wealth. It is situated at one end of Breach Candy,
 a posh residential and shopping area, now known as B. Desai Road. During the Navratra�s devotees from distant places 
throng the temple to pay their obeisance.</p>
<p style="color:#cc3300;font-weight:bold;">
SIDDHIVINAYAK TEMPLE</p>
<p style="color:#2b3e63;font-weight:bold;">
Located in the Prabha Devi area of Mumbai, this popular temple dedicated to Ganesh was rebuilt on the site
 of a 200-year old temple. It is dedicated to Lord Ganesha, the 'Lord of Beginnings' as per the Hindu Mythology.
 Built of black stone, the idol of Ganesh is two and a half feet in height and two feet in width. An unusual 
feature of the statue is that the trunk turns to the right, not often found on Ganesh idols. Tuesday is the main day of darshan and puja.</p>
 <p style="color:#2b3e63;font-weight:bold;">
The wooden main gate of the shrine is engraved with various images of Ashtavinayak, 
which symbolize the eight manifestations of Lord Ganesha. In the sanctum sanctorum, the gold-plated
 ceiling makes the worthy crown of the Lord. Ganpati is depicted with four arms, while holding a lotus flowe
r, an axe, platter of modakas and a garland of beads respectively in each of his hands.
 The image is flanked by Riddhi and Siddhi, two consorts of Lord Ganesha.</p>
</p>
<p style="color:#cc3300;font-weight:bold;">
JUHU BEACH </p>
<p style="color:#2b3e63;font-weight:bold;">
Juhu Beach is famous for its sprawling beach. It is surrounded by the Arabian Sea in the west, 
Santa Cruz and Ville Parle in the east. Juhu is one of the more affluent areas of Mumbai. Many
 bollywood stars own bungalows in Juhu. Many tourists make it a point to visit the beach when 
they come to Mumbai, as it is a relatively uncrowded free space in the city, although it does 
get crowded in the evenings and weekends. It is famous for its Mumbai bhelpuri, panipuri and 
of course pav bhaji. On the southern end of Juhu beach there are many luxury hotels and apartments.</p>





